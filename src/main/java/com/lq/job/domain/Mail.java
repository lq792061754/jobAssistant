package com.lq.job.domain;

public class Mail {
    private String mailCount;
    private String mailHead;
    private String mailBody;
    public String getMailCount() {
        return mailCount;
    }
    public void setMailCount(String mailCount) {
        this.mailCount = mailCount;
    }
    public String getMailHead() {
        return mailHead;
    }
    public void setMailHead(String mailHead) {
        this.mailHead = mailHead;
    }
    public String getMailBody() {
        return mailBody;
    }
    public void setMailBody(String mailBody) {
        this.mailBody = mailBody;
    }

}
