package com.lq.job.domain;

public class ExerciseTopic {
    private Integer et_id;
    private String et_name;
    private String et_time;
    private String et_from;
    private String et_kind;
    public Integer getEt_id() {
        return et_id;
    }
    public void setEt_id(Integer et_id) {
        this.et_id = et_id;
    }
    public String getEt_name() {
        return et_name;
    }
    public void setEt_name(String et_name) {
        this.et_name = et_name;
    }
    public String getEt_time() {
        return et_time;
    }
    public void setEt_time(String et_time) {
        this.et_time = et_time;
    }
    public String getEt_from() {
        return et_from;
    }
    public void setEt_from(String et_from) {
        this.et_from = et_from;
    }
    public String getEt_kind() {
        return et_kind;
    }
    public void setEt_kind(String et_kind) {
        this.et_kind = et_kind;
    }


}
